                                <h2 style="text-align: left;">La résiliation judiciaire du contrat de travail<br></h2>
                                <h3>Le cadre général<br></h3>
                                <p><strong>L’article 1217 du Code civil prévoit que :</strong><br>« La partie envers
                                    laquelle l’engagement n’a pas été exécuté, ou l’a été imparfaitement, peut :</p>
                                <ul>
                                    <li>Refuser d’exécuter ou suspendre l’exécution de sa propre obligation ;</li>
                                    <li>Poursuivre l’exécution forcée en nature de l’obligation ;</li>
                                    <li>Solliciter une réduction du prix ;</li>
                                    <li>Provoquer la résolution du contrat ;</li>
                                    <li>Demander réparation des conséquences de l’inexécution.</li>
                                </ul>
                                <p>Les sanctions qui ne sont pas incompatibles peuvent être cumulées, des dommages et
                                    intérêts peuvent toujours s’y ajouter. »<br>&nbsp;<br>Ainsi, les parties au contrat
                                    peuvent, pour rompre ce dernier, avoir recours à la procédure de résiliation
                                    judiciaire en invoquant sur la base de l’article 1247 du Code civil précité, le fait
                                    que l’autre partie n’a pas satisfait à son engagement.<br>&nbsp;<br>La jurisprudence
                                    admet la possibilité pour le salarié de saisir le conseil de prud’hommes afin
                                    d’obtenir la résiliation judiciaire de son contrat aux torts de l’employeur lorsque
                                    celui-ci n’exécute pas ses obligations contractuelles.<br>L’action en résiliation
                                    judiciaire du contrat de travail relève de la compétence exclusive du Conseil de
                                    prud’hommes.<br>&nbsp;<br>Contrairement à la prise d’acte, l’action en résiliation
                                    judiciaire n’entraine pas la rupture du contrat de travail. Le salarié continue donc
                                    de travailler normalement au sein de l’entreprise.<br>&nbsp;<br>Ainsi, si le juge
                                    prononce la résiliation du contrat, le contrat de travail est rompu à la date du
                                    prononcé de la décision judiciaire, et non à la date de saisine du juge, dès lors
                                    que le salarié est toujours au service de son employeur à cette date et que le
                                    contrat de travail n’a pas été rompu avant la décision prononçant la résiliation
                                    (Cass. Soc., 21 septembre 2016, n°14-30.056).<br>&nbsp;<br>L’employeur ne saurait
                                    rompre le contrat du travail du salarié une fois la résiliation judiciaire
                                    prononcée. En effet, en vertu du principe « rupture sur rupture ne vaut », lorsque
                                    la résiliation judiciaire du contrat de travail a été prononcée aux torts de
                                    l’employeur, le licenciement notifié ultérieurement par ce dernier est sans effet.
                                    Toutefois, si le licenciement est notifié après le jugement de résiliation mais
                                    avant la décision de la Cour d’appel saisie d’un recours de l’employeur, la Cour
                                    d’appel doit d’abord se prononcer sur le bienfondé de la résiliation (Cass. Soc., 5
                                    avril 2005, n°02-46.334). Ce n’est que si elle infirme la décision des premiers
                                    juges, c’est-à-dire si elle considère qu’il n’y a pas lieu de prononcer la
                                    résiliation judiciaire aux torts de l’employeur, que la Cour d’appel examinera
                                    éventuellement le bien-fondé du licenciement prononcé
                                    ultérieurement.<br>&nbsp;<br>Il en est de même pour ce qui est d’un licenciement
                                    prononcé postérieurement à l’introduction de la demande de résiliation judiciaire
                                    par le salarié mais avant que le juge ait statué. Dès lors que le salarié a continué
                                    de travailler et que l’employeur le licencie pour des faits survenus au cours de la
                                    poursuite du contrat, le juge doit d’abord rechercher si la demande de résiliation
                                    était justifiée (Cass. Soc., 26 mars 2014, n°12-21.372). Ce n’est que si le juge
                                    estime la demande de résiliation infondée qu’il statuera sur le bien-fondé du
                                    licenciement.<br>&nbsp;<br>En cas de demande de résiliation du contrat suivie d’une
                                    démission, cette dernière rompt le contrat et rend la demande initiale sans objet.
                                    Le salarié peut néanmoins obtenir réparation du préjudice subi. De même qu’il peut
                                    demander la requalification de sa démission en prise d’acte.<br>&nbsp;<br>Les
                                    manquements de l’employeur justifiant une action en résiliation
                                    judiciaire<br>&nbsp;<br>C’est au fur et à mesure que la jurisprudence fournit la
                                    liste des manquements « suffisamment graves ».<br>&nbsp;<br><strong>Tel est le cas
                                        par exemple :</strong></p>
                                <ul>
                                    <li>De la violation du principe d’égalité salariale (Cass. Soc., 23 mars 2011,
                                        n°09-70.607) ;</li>
                                    <li>Du non-paiement d’une prime conventionnelle d’ancienneté (Cass. Soc., 8 avril
                                        2010, n°09-41.134) ;</li>
                                    <li>De l’atteinte à la dignité du salarié en raison des propos que l’employeur avait
                                        tenus sur son odeur corporelle (Cass. Soc., 7 février 2012, n°10-18.686) ;</li>
                                    <li>Du non-respect du droit au repos hebdomadaire (Cass. Soc., 7 octobre 2003,
                                        n°01-44.635) ;</li>
                                    <li>Ou encore de l’absence de reprise de versement des salaires à l’expiration du
                                        délai d’un mois à l’issue de la visite de reprise (Cass. Soc., 20 septembre
                                        2006, n°05-42.930).</li>
                                </ul>
                                <p>Les effets de la résiliation judiciaire<br>&nbsp;<br>La résiliation judiciaire
                                    prononcée à l’initiative du salarié et aux torts de l’employeur produit les effets
                                    d’un licenciement sans cause réelle et sérieuse.<br>&nbsp;<br><strong>Le salarié
                                        peut alors prétendre :</strong></p>
                                <ul>
                                    <li>Aux dommages et intérêts prévus par les articles L. 1235-3 et L. 1235-5 du Code
                                        du travail, soit à l’indemnité de licenciement sans cause réelle et sérieuse ;
                                    </li>
                                    <li>Aux indemnités légales ou conventionnelles de licenciement et aux indemnités
                                        compensatrices de préavis et de congés payés.</li>
                                </ul>
                                <p>A noter que l’indemnité de préavis est toujours due en cas de résiliation aux torts
                                    de l’employeur, même si le salarié était dans l’incapacité d’exécuter son préavis,
                                    pour invalidité par exemple (Cass. Soc., 28 avril 2011,
                                    n°09-40.708).<br>&nbsp;<br>Par ailleurs, la résiliation judiciaire du contrat de
                                    travail prononcée aux torts de l’employeur, en raison notamment du harcèlement moral
                                    dont le salarié a été victime sur son lieu de travail produit les effets d’un
                                    licenciement nul. La qualification de licenciement nul emporte plusieurs
                                    conséquences financières. L’employeur alors verser au salarié les indemnités de
                                    rupture ainsi qu’une indemnité au moins égale à 6 mois de salaire (Cass. Soc., 20
                                    février 2013, n°11-26.560).</p>
