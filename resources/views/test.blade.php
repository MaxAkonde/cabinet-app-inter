                            <div>
                                <h1>Expatriation<br></h1>
                                <ul>
                                    <li>Le nombre de ressortissants français à l’étranger ne cesse d’augmenter. Avec 10%
                                        de hausse en moyenne, la mobilité internationale séduit de plus en plus de
                                        français. Le ministère des Affaires étrangères a estimé à plus de trois millions
                                        le nombre de ressortissants français établis à l'étranger fin 2013, selon les
                                        inscriptions enregistrées auprès des ambassades et consulats.<br></li>
                                    <li>Il existe différents types de contrats pour partir travailler à l’étranger. Le
                                        contrat de travail international a une architecture particulière. On dit qu’un
                                        contrat de travail est international lorsqu’il présente un ou plusieurs éléments
                                        d’extranéité, tenant à la nationalité des parties, au lieu d’exécution, de
                                        conclusion, etc…</li>
                                    <li>Selon les auteurs, le critère consiste dans le déplacement d’un salarié d’un
                                        Etat (Etat d’origine) vers un autre (Etat d’accueil) pour l’exécution de sa
                                        prestation de travail.</li>
                                    <li>Un certain nombre d’hypothèses correspondent à cette définition qu’il faut
                                        encore distinguer selon la forme que revêt la mutation, notamment sa durée : un
                                        bref détachement à l’étranger, une simple mission, une mise à disposition<br>
                                    </li>
                                    <li>
                                        <p>temporaire, ou une expatriation constituent autant de contrats de travail
                                            internationaux.<br><strong>Tel est le cas :</strong><br></p>
                                        <ul>
                                            <li>Quand une entreprise envoie l’un de ses salariés, travaillant
                                                jusqu’alors pour elle, à l’étranger, dans une entreprise ou un
                                                établissement<br></li>
                                            <li>Quand une entreprise appartenant à un groupe multinational envoie l’un
                                                de ses salariés dans une filiale à l’étranger, ou inversement, ou encore
                                                en cas de transfert d’une filiale vers une autre<br></li>
                                        </ul>
                                    </li>
                                </ul>
                            </div>
