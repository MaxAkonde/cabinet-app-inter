                                    <li>le refus d’exécuter le travail pour lequel le salarié a été engagé ou le refus
                                        d’exécuter un ordre de l’employeur (Cass. Soc., 1er avril 2003, n°00-46.833),
                                    </li>
                                    <li>le refus d’accepter la modification des conditions de travail.</li>
                                </ul>
                                <h2>Procédure :</h2>
