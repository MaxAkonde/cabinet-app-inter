                                <h2 style="text-align: left;">Motif Disciplinaire<br></h2>
                                <p>La faute disciplinaire est constituée par la violation des règles de discipline de
                                    l’entreprise. En conformité avec l’article L 1333-2 du Code du travail, le conseil
                                    de prud’hommes est compétent pour « annuler une sanction irrégulière en la forme ou
                                    injustifiée ou disproportionnée à la faute commise ».<br>Néanmoins, le juge n’a pas
                                    la possibilité d’annuler le licenciement (article L 1333-3 du Code du travail). Le
                                    juge peut uniquement apprécier l’existence d’une cause réelle et sérieuse. La Cour
                                    de Cassation a jugé que « dès lors qu’aucune faute n’est retenue à l’encontre du
                                    salarié, le licenciement se trouve dépourvu de cause réelle et sérieuse » (Cass.
                                    soc., 28 nov. 2000, no 98-43.029, Bull. civ. V, no 115, p. 87).<br>&nbsp;<br>En
                                    conformité avec l’article L 1235-5 du Code du travail, la sanction du licenciement
                                    illégitime est l’octroi de dommages-intérêts au
                                    salarié.<br>&nbsp;<br><strong>Exemples des fautes disciplinaires :</strong></p>
                                <ul>
                                    <li>l’abandon de poste, sans motif légitime (Cass. Soc., 27 mai 1998, n°95-44.218),
