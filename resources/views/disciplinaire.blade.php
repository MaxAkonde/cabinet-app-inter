                                    de même nature n'ayant pas d'incidence, immédiate ou non, sur la présence dans
                                    l'entreprise, la fonction, la carrière ou la rémunération du salarié.<br>Lors de son
                                    audition, le salarié peut se faire assister par une personne de son choix
                                    appartenant au personnel de l'entreprise.<br>Au cours de l'entretien, l'employeur
